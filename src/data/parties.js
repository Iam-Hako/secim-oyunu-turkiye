// Yerel hafızadaki özel partileri getir
export function getCustomParties() {
  const saved = localStorage.getItem('custom_parties');
  try {
    return saved ? JSON.parse(saved) : [];
  } catch (e) {
    console.error("Custom parties parse error", e);
    return [];
  }
}

// Çok Oyunculu modda odadaki başka birinin özel partisini kendi cihazımıza geçici/kalıcı olarak kaydeder
export function syncCustomPartyToLocal(partyObj) {
  if (!partyObj || !partyObj.id.startsWith('custom_')) return;
  const current = getCustomParties();
  if (!current.find(p => p.id === partyObj.id)) {
    const updated = [...current, partyObj];
    localStorage.setItem('custom_parties', JSON.stringify(updated));
  }
}

// Tüm partileri (Statik + Özel) birleştirip döndür
export function getAllParties() {
  return [...STATIC_PARTIES, ...getCustomParties()];
}

