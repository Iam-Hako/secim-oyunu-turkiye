// Parti rengini ID'ye göre getir
export function getPartyColor(partyId) {
  const parties = getAllParties();
  const party = parties.find(p => p.id === partyId);
  return party ? party.color : '#666666';
}

// Parti bilgilerini ID'ye göre getir
export function getPartyById(partyId) {
  const parties = getAllParties();
  return parties.find(p => p.id === partyId);
}
