// Başlangıç oylarını TÜRKİYE SİYASİ GERÇEKLİĞİNE göre dağıt
export const calculateInitialVotes = (provinces, parties) => {
  const votes = {};
  provinces.forEach(p => {
    votes[p.id] = {};
    
    let totalWeight = 0;
    const weights = parties.map(party => {
      // 1. ANA BAZ (Partilerin Türkiye genel gücü)
      let weight = party.initialVote || 5;

      // 2. BÖLGESEL MODİFİKATÖRLER
      if (p.region === 'Ege' || p.region === 'Marmara') {
         if (party.id === 'chp') weight += 15;
         if (party.id === 'iyi') weight += 5;
      }
      if (p.region === 'İç Anadolu' || p.region === 'Karadeniz') {
         if (party.id === 'akp') weight += 15;
         if (party.id === 'mhp') weight += 8;
      }
      if (p.region === 'Güneydoğu Anadolu') {
         if (party.id === 'dem') weight += 45;
         if (party.id === 'akp') weight += 10;
      }
      if (p.region === 'Doğu Anadolu') {
         if (party.id === 'dem') weight += 25;
         if (party.id === 'akp') weight += 15;
      }

      // 3. ŞEHİR BAZLI ÖZEL DURUMLAR (Override)
      if (p.name === 'İzmir' && party.id === 'chp') weight += 25;
      if (p.name === 'Konya' && party.id === 'akp') weight += 35;
      if (p.name === 'Diyarbakır' && party.id === 'dem') weight += 50;
      if (p.name === 'Rize' && party.id === 'akp') weight += 40;
      if (p.name === 'Tunceli') {
         if (party.id === 'chp') weight += 20;
         if (party.id === 'dem') weight += 20;
      }
      if (p.name === 'İstanbul' || p.name === 'Ankara') {
         if (party.id === 'akp') weight += 10;
         if (party.id === 'chp') weight += 10;
      }

      // 4. RANDOM JITTER (Her oyun biraz farklı başlasın)
      weight += Math.random() * 5;

      totalWeight += weight;
      return weight;
    });

    // 5. NORMALİZASYON (%100'e sabitle)
    let currentTotal = 0;
    parties.forEach((party, idx) => {
      let share = Math.round((weights[idx] / totalWeight) * 100);
      if (idx === parties.length - 1) {
        share = 100 - currentTotal;
      }
      votes[p.id][party.id] = Math.max(0, share);
      currentTotal += share;
    });
  });
  return votes;
};

// Bir ildeki kazanan partiyi bul (party.id standardıyla)
export const getLeadingParty = (provinceVotes, parties) => {
  if (!provinceVotes) return null;
  let max = -1;
  let leadingId = null;
  
  Object.entries(provinceVotes).forEach(([partyId, vote]) => {
    if (vote > max) {
      max = vote;
      leadingId = partyId;
    }
  });
  
  return parties.find(p => p.id === leadingId);
};

// Bir ildeki oyları güncekle (Toplamı 100'de tutarak)
export const applyVoteChange = (votes, provinceId, partyId, amount) => {
  if (!votes[provinceId]) return votes;
  
  const province = { ...votes[provinceId] };
  const oldVal = province[partyId] || 0;
  const newVal = Math.min(100, Math.max(0, oldVal + amount));
  const diff = newVal - oldVal;
  
  province[partyId] = newVal;
  
  // Diğer partilerden orantılı düşerek toplamı 100'de sabitle
  const otherParties = Object.keys(province).filter(id => id !== partyId);
  const totalOthers = otherParties.reduce((sum, id) => sum + province[id], 0);
  
  if (totalOthers > 0) {
    otherParties.forEach(id => {
      const share = province[id] / totalOthers;
      province[id] = Math.max(0, province[id] - (diff * share));
    });
  }

  // Yuvarlama sonrası toplamı tekrar kontrol et ve 100'e eşitle
  let currentTotal = Object.values(province).reduce((a, b) => a + b, 0);
  if (currentTotal !== 100) {
    const lastPartyId = otherParties[0] || partyId;
    province[lastPartyId] += (100 - currentTotal);
  }

  return { ...votes, [provinceId]: province };
};
